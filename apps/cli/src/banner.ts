import { cyan, dim } from "./colors.js";

const ANSI_RE = /\x1b\[[0-9;]*m/g;
const stripAnsi = (s: string): string => s.replace(ANSI_RE, "");

export interface BannerInput {
  version: string;
  model: string;
  cwd: string;
  home?: string;
}

export function renderBanner(opts: BannerInput): string {
  const home = opts.home ?? "";
  const cwdDisplay =
    home && (opts.cwd === home || opts.cwd.startsWith(home + "/"))
      ? "~" + opts.cwd.slice(home.length)
      : opts.cwd;

  const lines = [
    `${cyan(">_")} Nova Coding Agent ${dim(`(v${opts.version})`)} `,
    "",
    cyan("‚Ėą‚Ėą‚Ėą‚ēó   ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚ēó   ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēó "),
    cyan("‚Ėą‚Ėą‚Ėą‚Ėą‚ēó  ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚ēź‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēó"),
    cyan("‚Ėą‚Ėą‚ēĒ‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĎ"),
    cyan("‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚ēó‚Ėą‚Ėą‚ēĎ‚Ėą‚Ėą‚ēĎ   ‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚ēó ‚Ėą‚Ėą‚ēĒ‚ēĚ‚Ėą‚Ėą‚ēĒ‚ēź‚ēź‚Ėą‚Ėą‚ēĎ"),
    cyan("‚Ėą‚Ėą‚ēĎ ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚ēĎ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ ‚ēö‚Ėą‚Ėą‚Ėą‚Ėą‚ēĒ‚ēĚ ‚Ėą‚Ėą‚ēĎ  ‚Ėą‚Ėą‚ēĎ"),
    cyan("‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēź‚ēź‚ēĚ ‚ēö‚ēź‚ēź‚ēź‚ēź‚ēź‚ēĚ   ‚ēö‚ēź‚ēź‚ēź‚ēĚ  ‚ēö‚ēź‚ēĚ  ‚ēö‚ēź‚ēĚ"),
    "",
    `${dim("model:")}     ${opts.model}    ${dim("/model to change")}`,
    `${dim("directory:")} ${cwdDisplay}`
  ];

  const inner = Math.max(...lines.map((l) => stripAnsi(l).length));
  const horiz = "‚ĒÄ".repeat(inner + 2);
  const pad = (l: string): string => {
    const visible = stripAnsi(l).length;
    return `‚Ēā ${l}${" ".repeat(inner - visible)} ‚Ēā`;
  };

  return [`‚ē≠${horiz}‚ēģ`, ...lines.map(pad), `‚ēį${horiz}‚ēĮ`].join("\n");
}
